#include "wishbone_memory_bank.h"
#include "wishbone_driver.h"
#include "truss.h"

wishbone::wishbone_memory_bank::wishbone_memory_bank (const std::string n, const std::string& path, 
						      wishbone_driver* driver) : 
     teal::memory::memory_bank (path), wishbone_driver_ (driver), log_ (n) 
     {truss_assert (driver);}

void wishbone::wishbone_memory_bank::from_memory (teal::uint64 address, teal::reg* returned) { *returned = wishbone_driver_->read8 (address);}

void wishbone::wishbone_memory_bank::to_memory (teal::uint64 address, const teal::reg& value) {wishbone_driver_->write8 (address, value);}
